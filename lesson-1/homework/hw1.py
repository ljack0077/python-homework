import math
## 1. Square Problem
### Solution
print("--- Task 1: Square ---")
side = 5  
perimeter = 4 * side
area = side * side

print(f"The side of the square is: {side}")
print(f"The perimeter is: {perimeter}")
print(f"The area is: {area}")
print("-" * 20) 

## 2. Circle Problem
### Solution
print("--- Task 2: Circle ---")
diameter = 10  
length = math.pi * diameter

print(f"The diameter of the circle is: {diameter}")
print(f"The length (circumference) is: {length}")
print("-" * 20)

## 3. Mean Problem
### Solution
print("--- Task 3: Mean ---")
a = 10  
b = 20  
mean = (a + b) / 2
print(f"The first number (a) is: {a}")
print(f"The second number (b) is: {b}")
print(f"The mean is: {mean}")
print("-" * 20)

## 4. Sum, Product, and Squares
### Solution
print("--- Task 4: Sum, Product, Squares ---")
a = 3  
b = 4  

sum_result = a + b
product_result = a * b
square_of_a = a * a  
square_of_b = b * b  

print(f"The first number (a) is: {a}")
print(f"The second number (b) is: {b}")
print(f"Their sum is: {sum_result}")
print(f"Their product is: {product_result}")
print(f"The square of 'a' is: {square_of_a}")
print(f"The square of 'b' is: {square_of_b}")
print("-" * 20)
